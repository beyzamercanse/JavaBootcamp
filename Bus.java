package VARIABLES;

public class Bus {

    public static void main(String[] args) {

        int passengers = 0;
        passengers = passengers + 9;
        System.out.println(passengers);
        passengers = passengers - 5;
        System.out.println(passengers);
        passengers  =  passengers  - 4;
        System.out.println(passengers);

        // +=  -= *=  /= operators update by adding the value on the right

        passengers += 9;
        System.out.println(passengers);
        passengers -= 5;
        System.out.println(passengers);
        passengers -= 4;
        System.out.println(passengers);

    }
}
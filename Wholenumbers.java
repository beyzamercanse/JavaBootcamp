package VARIABLES;

public class Wholenumbers {

    public static void main(String[] args) {
        //int = stores whole numbers
        int numberOfFans = 6803;
        System.out.println("There are " + numberOfFans + " fans at the Quidditch games" );

        long globalPopulation = 30000000000L;   //int only takes until 2 billion something, if the value is more than that; use long
        // long : use number+ add L in the end.
        System.out.println("There are " + globalPopulation + " people on earth ");

        long dailyGoogleSearches = 5000000000L;
        System.out.println("Daily, there are " + dailyGoogleSearches + " amount of people using Google. ");


    }
    
}
